[RequireComponent(typeof(Canvas))]
public abstract class BaseCanvas : MonoBehaviour {

	// Methods

	public virtual void Show() {
		gameObject.SetActive(true);
	}
	public virtual void Hide(bool keepState = false) {
		gameObject.SetActive(false);
	}
}
